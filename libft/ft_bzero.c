#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tab;
	int				i;

	tab = (unsigned char *)s;
	i = 0;
	while (n)
	{
		tab[i] = '\0';
		i ++;
		n --;
	}
}
